#include<bits/stdc++.h>
using namespace std;
#define  ll long long
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int n; cin>>n;
        priority_queue<int>pq;
        pq.push(0);
        for(int i=0;i<n;i++)
        {
            int x; cin>>x;
            if(pq.top()!=x)
            pq.push(x);
        }
        int ans=pq.top();
        pq.pop();
        ans+=pq.top();
        cout<<ans<<endl;


    }
    return 0;
}