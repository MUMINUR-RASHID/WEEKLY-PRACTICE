#include<bits/stdc++.h>
using namespace std;
typedef long long ll;

bool is_big(ll s,ll m)
{
    ll temp=m;
    ll sum=0;
    while(temp)
    {
        ll r=temp%10;
        sum+=r;
        temp=temp/10;
    }
    return (m-sum)>=s;
}
int main()
{
    
         
        ll n,s;
        cin>>n>>s;
        ll l=10, r=n;
        ll an=0;
        while(l<=r)
        {
            ll m=l+(r-l)/2;
            if(is_big(s,m))
            {
                an=m;
                r=m-1;
            }
            else 
            {
                l=m+1;
            }
        }
        if(an)
        cout<<(n-an)+1<<endl;
        else cout<<an<<endl;
    
    
}